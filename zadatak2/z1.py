dan = int(input("Izaberi broj od 1 do 7"))

if dan == 1:
    print("ponedjeljak")
elif dan == 2:
    print("utorak")
elif dan == 3:
    print("srijeda")
elif dan == 4:
    print("četvrtak")
elif dan == 5:
    print("petak")
elif dan == 6:
    print("subota")
elif dan == 7:
    print("nedjelja")
else:
    print("Pogrešan broj")



























