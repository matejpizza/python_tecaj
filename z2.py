a = int(input("Unesi prvi broj: "))
b = int(input("Unesi drugi broj: "))


if a > b:
    print("Prvi je veći")
elif b > a:
    print("Drugi je veći")
elif a == b:
    print("Jedanki su")
else:
    print("Nema značenja")






# Program pita korisnika dva broja.
# Ako je prvi veći → "Prvi je veći"
# Ako je drugi veći → "Drugi je veći"
# Ako su jednaki → "Jednaki su"